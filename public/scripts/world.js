let activeId = null;

function flagImg(code, size) {
  size = size || 24;
  return '<span class="fi fi-' + code + '" style="width:' + (size * 1.33) + 'px;height:' + size + 'px;border-radius:3px;background-size:cover;display:inline-block;vertical-align:middle;"></span>';
}

function renderGrid() {
  document.getElementById('country-grid').innerHTML = countries.map(c => `
    <div class="country-card${activeId === c.id ? ' active' : ''}" data-id="${c.id}">
      <span class="flag">${flagImg(c.flag, 28)}</span>
      <div class="c-name">${c.name}</div>
      <div class="c-sub">${c.sub}</div>
    </div>`).join('');

  document.querySelectorAll('.country-card').forEach(card => {
    card.addEventListener('click', () => selectC(card.dataset.id));
  });
}

function selectC(id) {
  if (activeId === id) {
    activeId = null;
    document.getElementById('detail-panel').classList.remove('show');
    document.getElementById('empty-state').style.display = 'block';
    renderGrid();
    return;
  }
  activeId = id;
  renderGrid();
  const c = countries.find(x => x.id === id);
  renderDetail(c);
  document.getElementById('empty-state').style.display = 'none';
  document.getElementById('detail-panel').classList.add('show');
  setTimeout(() => document.getElementById('detail-panel').scrollIntoView({ behavior: 'smooth', block: 'nearest' }), 80);
}

